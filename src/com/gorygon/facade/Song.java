package com.gorygon.facade;

public class Song {
	String name;
	String guitarPart;
	String bassPart;
	String drumsPart;
	String lyrics;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getGuitarPart() {
		return guitarPart;
	}

	public void setGuitarPart(String guitarPart) {
		this.guitarPart = guitarPart;
	}

	public String getBassPart() {
		return bassPart;
	}

	public void setBassPart(String bassPart) {
		this.bassPart = bassPart;
	}

	public String getDrumsPart() {
		return drumsPart;
	}

	public void setDrumsPart(String drumsPart) {
		this.drumsPart = drumsPart;
	}

	public String getLyrics() {
		return lyrics;
	}

	public void setLyrics(String lyrics) {
		this.lyrics = lyrics;
	}
}
